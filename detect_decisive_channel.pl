pair_prefered_by_singleton( [ X, Y ], P, J, Pj ):-
	 pp( P ),
	 x( X ), x( Y ), X \= Y,
	 nth1( J, P, Pj ),
	 p( [ X, Y ], Pj ),
	 \+ (
		 nth1( K, P, Pk ),
		 K \= J,
		 p( [ X, Y ], Pk )
	 ).

decisive_channel( P, J, [ X, Y ] -> [ Z, W ] ):-
	 pair_prefered_by_singleton( [ X, Y ], P, J, Pj ),
	 pair_prefered_by_singleton( [ Z, W ], P, J, Pj ),
	 [ X, Y ] \= [ Z, W ],
	 \+ (
		 f( F, [ P ], swf_axiom ),
		 F = [ P - T ],
		 agree( +, [ X, Y ], [ T, Pj ] ),
		 \+ agree( +, [ Z, W ], [ T, Pj ] )
	 ).

