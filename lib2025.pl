unilateral_unit_deviation_from( X, Z, I, K, Y ):-
	 member( Y, Z ),
	 unilateral_unit_deviation_from( X, I, K, Y ).

unilateral_unit_deviation_from( X, I, K, Y ):-
	 var( Y ),
	 unilateral_deviation_from( X, Y, [ K, Z, W | _ ] ),
	 member( W, [ Z - I, Z + I ] ).

unilateral_unit_deviation_from( X, I, K, Y ):-
	 \+ var( Y ),
	 nth1( K, X, Z ),
	 nth1( K, Y, W ),
	 round( I = abs( Z - W ), 10 ),
	 total_absolute_difference( X, Y, T ),
	 I >= T.

unilateral_deviation_from( X, Y, [ K, Z, W, F, B ] ):-
	 nth1( K, X, Z ),
	 nth1( K, Y, W ),
	 append( F, [ Z | B ], X ),
	 append( F, [ W | B ], Y ).

total_absolute_difference( X, Y, T ):-
	 findall( D, (
		 nth1( J, X, XJ ),
		 nth1( J, Y, YJ ),
		 D = abs( XJ - YJ )
	 ), DL ),
	 sum_list( DL, T ).

