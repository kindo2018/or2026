% an advanced version: sawing the boundaries. 

function_value( F, X, V ):-
	 G =.. [ F, V | X ],
	 G.

evaluate( [ ] = [ ] ).
evaluate( [ X | Y ] = [ A | B ] ):-
	 round3( X = A ),
	 evaluate( Y = B ).


tangent_neighbor( F, [ X, Y ], [ V, V1 ], I, K ):-
	 function_value( F, X, V ),
 	 unilateral_unit_deviation_from( X, I, K, Y0 ),
	 evaluate( Y = Y0 ),
	 function_value( F, Y, V1 ),
	 V1 \= V,
	 \+ another_neighbor_contacts( F, Y, I, _, _, X, V ).

another_neighbor_contacts( F, Y, I, J, Z, X, V ):-
	 unilateral_unit_deviation_from( Y, I, J, Z0 ),
	 evaluate( Z = Z0 ),
	 Z0 \= X,
	 function_value( F, Z, V ).

sawing_boundary_neighbor( F, [ X, Y, Z ], [ V, V1 ], I, [ K, J ] ):-
	 function_value( F, X, V ),
 	 unilateral_unit_deviation_from( X, I, K, Y0 ),
	 evaluate( Y = Y0 ),
	 function_value( F, Y, V1 ),
	 V1 \= V,
 	 unilateral_unit_deviation_from( Y, I, J, Z0 ),
	 evaluate( Z = Z0 ),
	 Z \= X,
	 function_value( F, Z, V ).

