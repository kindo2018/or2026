decompose_intervals( [ X | Z ], [ P, R | Q ], I ):-
	 decompose_intervals( Z, [ R | Q ], I ),
	 findall_connected_to( X, [ X | Z ], I, P ),
	 intersection( R, P, [ ] ).

decompose_intervals( [ X | Z ], [ U | Q ], I ):-
	 decompose_intervals( Z, [ R | Q ], I ),
	 findall_connected_to( X, [ X | Z ], I, P ),
	 \+ intersection( P, R, [ ] ),
	 union( R, P, U ).

