step_value_between( L, U, _, _, Val ) :- 
	 \+ var( Val),
	 Val >= L,
	 Val =< U.

% (1) ascending order

step_value_between( L, U, Interval, +, Val ) :- 
	 var( Val),
	 K is integer( ( U - L ) / Interval ), 
	 between( 0, K, I ),
	 round3( Val = L + Interval * I ).

% (2) descending order

step_value_between( L, U, Interval, -, Val ) :- 
	 var( Val),
	 K is integer( ( U - L ) / Interval ), 
	 between( 0, K, I ),
	 round3( Val = U - Interval * I ).

step_value_between( L, U, Interval, Val ) :- 
	 step_value_between( L, U, Interval, +, Val ). 

