sequentially_extract( [ X | Y ], L, [ J | O ] ):-
	 nth1( J, L, X ),
	 append( L0, [ X | L1 ], L ),
	 append( L0, L1, L2 ),
	 sequentially_extract( Y, L2, O ).

sort_with_indices( L, L1, Order ):-
	 sort( L, L1 ),
	 sequentially_extract( L1, L, Order ).

permutate_by_indices( L, Order, L1 ):-
	 length( L, N ),
	 permutate_by_indices( L, N, Order, L1 ).

permutate_by_indices( L, N, Order, L1 ):-
	 make_order_if_needed( N, Order ),
	 findall( X, (
		 member( J, Order ),
		 nth1( J, L, X )
	 ), L1 ).

make_order_if_needed( N, Order ):-
	 \+ var( Order ),
	 length( Order, N ).

make_order_if_needed( N, Order ):-
	 var( Order ),
	 integer( N ),
	 findall( I, between( 1, N, I ), L ),
	 permutation( L, Order ).

